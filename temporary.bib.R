               (deep venous thrombosis and coagulopathy). Analysis revealed a
               significant breakpoint at 45 years of age for mortality,
               decubitus ulcer, and renal failure (all p values <0.05).
               Infectious complications (sepsis, wound infection, and abscess)
               all peaked between 45 years and 65 years and then declined with
               increasing age. CONCLUSIONS: We document that elderly trauma
               patients suffer the same complications as their younger
               counterparts, albeit at a different rate. More importantly, we
               identified a ``breakpoint'' of increased risk of complications
               and mortality at greater than 45 years. Although the mechanisms
               behind these observations remain unknown, understanding their
               unique patterns may allow appropriate allocation of resources
               and focus research efforts on interventions that should improve
               outcomes.",
