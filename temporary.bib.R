  abstract  = "BACKGROUND: Trauma centers are caring for increased proportions
               of elderly patients. Although age and Injury Severity Score are
               independently associated with mortality, trauma centers were
               originally designed to care for seriously injured patients
               without age-specific guidelines. We hypothesized that elderly
               patients would have different complication patterns than their
               younger counterparts. METHODS: The trauma registry of an
               American College of Surgeons -verified Level I trauma center was
               queried for all patients older than 14 years admitted between
               January 2005 and December 2008. Mechanism, mortality, and
               complications were evaluated after dividing patients into eight
               age groups. RESULTS: Of the 15,223 patients, 13\% were elderly
               ($\geq$65), and 86\% were injured via a blunt mechanism.
               Increasing age correlated with fatality (all Injury Severity
               Scores), end-organ failure, and thromboembolic complications
               (deep venous thrombosis and coagulopathy). Analysis revealed a
               significant breakpoint at 45 years of age for mortality,
               decubitus ulcer, and renal failure (all p values <0.05).
               Infectious complications (sepsis, wound infection, and abscess)
               all peaked between 45 years and 65 years and then declined with
               increasing age. CONCLUSIONS: We document that elderly trauma
               patients suffer the same complications as their younger
               counterparts, albeit at a different rate. More importantly, we
               identified a ``breakpoint'' of increased risk of complications
               and mortality at greater than 45 years. Although the mechanisms
               behind these observations remain unknown, understanding their
               unique patterns may allow appropriate allocation of resources
               and focus research efforts on interventions that should improve
               outcomes.",
